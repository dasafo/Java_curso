
public class DeclaracionesOperadores_3_II {

	public static void main(String[] args) {
		
		final double apulgadas=2.54; //con 'final' hacemos ese valor una constante que no se puede modificar
		
		double cm=6;
		
		double resultado=cm/apulgadas;
		
		
		System.out.println("En " + cm + " cm hay " + resultado + " pulgadas.");
		
		

	}

}
